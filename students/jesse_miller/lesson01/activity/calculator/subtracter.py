#!/usr/bin/env python3
'''
Subtraction module for calculator
'''
#pylint: disable=R0903
class Subtracter():
    '''
    Addition class for a more complex calculator
    '''

    @staticmethod
    def calc(operand_1, operand_2):
        '''
        This does the heavy lifting for subtracting
        '''
        return operand_1 - operand_2
